package com.example.mysocialuni;

public class EventModel {
    public String day;
    public String month;
    public String title;
    public String clubAndTime;

    public EventModel(String day, String month, String title, String clubAndTime) {
        this.day = day;
        this.month = month;
        this.title = title;
        this.clubAndTime = clubAndTime;
    }
}
